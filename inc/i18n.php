function app_localize_output(string $output): string
{
    if (app_locale() !== 'en' || $output === '') return $output;
    return strtr($output, app_english_html_replacements());
}

function app_enable_output_localization(): void
{
    static $enabled = false;
    if ($enabled || PHP_SAPI === 'cli') return;
    $page = basename((string)($_SERVER['SCRIPT_NAME'] ?? ''));
    $pages = ['index.php', 'dashboard.php', 'succesLogged.php', 'campaigns.php', 'lists.php', 'teams.php', 'users.php', 'settings.php', 'companies.php', 'devices.php', 'providers.php', 'credits.php', 'firewall.php', 'logs.php', 'system_logs.php', 'access_denied.php', 'adduser.php'];
    if (!in_array($page, $pages, true)) return;
    $enabled = true;
    ob_start('app_localize_output');
}
