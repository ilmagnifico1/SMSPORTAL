<?php

const APP_SUPPORTED_LOCALES = ['it', 'en'];
const APP_DEFAULT_LOCALE = 'it';

function app_normalize_locale(mixed $locale): string
{
    $locale = strtolower(substr(trim((string)$locale), 0, 2));
    return in_array($locale, APP_SUPPORTED_LOCALES, true) ? $locale : APP_DEFAULT_LOCALE;
}

function app_locale(): string
{
    return app_normalize_locale($_SESSION['preferred_language'] ?? APP_DEFAULT_LOCALE);
}

function app_locale_names(): array
{
    return ['it' => 'Italiano', 'en' => 'English'];
}

function app_translations(): array
{
    static $translations = null;
    if ($translations !== null) return $translations;

    $translations = [
        'it' => [
            'language' => 'Lingua',
            'change_language' => 'Cambia lingua',
        ],
        'en' => [
            'language' => 'Language',
            'change_language' => 'Change language',
        ],
    ];
    return $translations;
}

function t(string $key, array $replacements = []): string
{
    $catalog = app_translations();
    $text = (string)($catalog[app_locale()][$key] ?? $catalog[APP_DEFAULT_LOCALE][$key] ?? $key);
    foreach ($replacements as $name => $value) $text = str_replace(':' . $name, (string)$value, $text);
    return $text;
}

