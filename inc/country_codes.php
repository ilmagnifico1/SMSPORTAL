function sms_country_flag(string $iso): string {
    $iso = strtoupper(substr(trim($iso), 0, 2));
    if (!preg_match('/^[A-Z]{2}$/', $iso)) {
        return '🌐';
    }
    return html_entity_decode('&#' . (127397 + ord($iso[0])) . ';&#' . (127397 + ord($iso[1])) . ';', ENT_NOQUOTES, 'UTF-8');
}

function sms_country_for_prefix(string $prefix, string $destination = ''): ?array {
    $normalizedPrefix = preg_replace('/\D+/', '', $prefix) ?: '';
    if ($normalizedPrefix === '') {
        return null;
    }
    $matches = [];
    foreach (sms_country_codes() as $country) {
        if ((preg_replace('/\D+/', '', (string)$country['code']) ?: '') === $normalizedPrefix) {
            $matches[] = $country;
        }
    }
    if ($destination !== '') {
        foreach ($matches as $country) {
            if (stripos((string)$country['label'], trim($destination)) !== false) {
                return $country;
            }
        }
    }
    return $matches[0] ?? null;
}
